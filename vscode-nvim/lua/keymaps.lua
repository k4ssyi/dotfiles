keymap.set("i", "<C-k>", "<Up>", { noremap = true })
keymap.set("i", "<C-h>", "<Left>", { noremap = true })
keymap.set("i", "<C-l>", "<Right>", { noremap = true })
keymap.set("n", "あ", "a", { noremap = true })
